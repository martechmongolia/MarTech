// ─── buildAgentPrompt ───────────────────────────────────────
// sessionType болон constraintText-г systemPrompt-д нэмэлт заавар болгон оруулна.
// Six Hats горимд агент бүрийг өнгөт малгайн үүргийг гүйцэтгүүлнэ.
export function buildAgentPrompt(
  agent: Agent,
  topic: string,
  context: string,
  sessionType: string = 'free_flow',
  constraintText?: string
): string {
  let modeInstruction = '';

  if (sessionType === 'six_hats') {
    const hatMap: Record<string, string> = {
      marketer:     '🟡 Yellow Hat — зөвхөн БОЛОМЖ, давуу тал, оптимист өнцгөөс хариул',
      analyst:      '⚪ White Hat — зөвхөн БАРИМТ, тоо, нотлогдсон мэдээллээр хариул',
      skeptic:      '⚫ Black Hat — зөвхөн ЭРСДЭЛ, сул тал, яагаад болохгүйг хариул',
      idealist:     '🟢 Green Hat — хязгааргүй БҮТЭЭЛЧ санаа, дурын боломжийг дурд',
      psychologist: '🔴 Red Hat — СЭТГЭЛ, интуиц, хэрэглэгчийн мэдрэмжийг тайлбарла',
      moderator:    '🔵 Blue Hat — хэлэлцүүлгийг УДИРДА, нэгтгэ, дараагийн алхам тодорхойл',
    };
    modeInstruction = `\n\nОДОО ${hatMap[agent.id] ?? ''} өмсч байна. Зөвхөн тэр өнцгөөс хариул.`;
  }

  if (sessionType === 'round_robin') {
    modeInstruction = '\n\nРОУНД РОБИН горим: Өмнөх оролцогчийн санааг заавал нэг алхам гүнзгийрүүлж, дараа нь ШИНЭ санаа нэм.';
  }

  if (sessionType === 'disney') {
    // Phase нь turn_state-аас ирнэ — тусдаа зааврыг нэм
    modeInstruction = '\n\nДИСНЕЙ АРГА: Одоогийн phase-д яг тохируулж хариул.';
  }

  if (sessionType === 'scamper') {
    modeInstruction = '\n\nSCAMPER горим: Санаагаа SCAMPER-ийн нэг lens-ээр (Substitute/Combine/Adapt/Modify/Put to other uses/Eliminate/Reverse) тайлбарла.';
  }

  if (constraintText) {
    modeInstruction += `\n\nХЯЗГААРЛАЛТ (заавал мөрд): ${constraintText}`;
  }

  return agent.systemPrompt
    .replace('{topic}', topic)
    .replace('{context}', context) + modeInstruction;
}
