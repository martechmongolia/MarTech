// ─── Six Hats малгайн өнгө (PA-hat) ───────────────────────────────────────
const HAT_COLORS: Record<string, string> = {
  marketer:     '#F1C40F', // Yellow Hat
  analyst:      '#BDC3C7', // White/Silver Hat
  skeptic:      '#2C3E50', // Black/Dark Hat
  idealist:     '#2ECC71', // Green Hat
  psychologist: '#E74C3C', // Red Hat
  moderator:    '#3498DB', // Blue Hat
};

// ─── Component ─────────────────────────────────────────────
interface PixelArtAvatarSVGProps {
  agentId: AgentId | "user";
  size?: number;
  isSpeaking?: boolean;
  isActive?: boolean;
  className?: string;
  sessionType?: string;
}

export function PixelArtAvatarSVG({
  agentId,
  size = 64,
  isSpeaking = false,
  isActive = false,
  className = "",
  sessionType,
}: PixelArtAvatarSVGProps) {
  const art = PIXEL_ART_MAP[agentId] ?? USER_ART;
  const _pixelSize = size / 16;
  // Six Hats горимд агент бүрийн avatar дээр өнгөт малгай харуулна
  const hatColor = sessionType === 'six_hats' && agentId !== 'user'
    ? (HAT_COLORS[agentId] ?? null)
    : null;

  return (
    <div className={`relative inline-block ${className}`} style={{ width: size, height: size }}>
      {/* Glow ring (PA-09) */}
      {(isSpeaking || isActive) && (
        <motion.div
          className="absolute inset-0 rounded-full pointer-events-none"
          style={{
            boxShadow: isSpeaking
              ? "0 0 0 3px rgba(255,255,255,0.9), 0 0 16px 4px rgba(255,255,255,0.4)"
              : "0 0 0 2px rgba(255,255,255,0.4)",
            borderRadius: "50%",
          }}
          animate={
            isSpeaking
              ? { scale: [1, 1.08, 1], opacity: [0.8, 1, 0.8] }
              : { scale: 1, opacity: 0.6 }
          }
          transition={
            isSpeaking
              ? { repeat: Infinity, duration: 1.2, ease: "easeInOut" }
              : {}
          }
        />
      )}

      {/* Scale pulse (PA-09) */}
      <motion.div
        animate={isSpeaking ? { scale: [1, 1.05, 1] } : { scale: 1 }}
        transition={
          isSpeaking
            ? { repeat: Infinity, duration: 1.2, ease: "easeInOut" }
            : {}
        }
      >
        <svg
          width={size}
          height={size}
          viewBox="0 0 16 16"
          xmlns="http://www.w3.org/2000/svg"
          style={{ imageRendering: "pixelated" }}
        >
          {art.pixels.map((row, y) =>
            row.map((colorIdx, x) => {
              if (colorIdx === 0) return null;
              const fill = art.palette[colorIdx];
              return (
                <rect
                  key={`${x}-${y}`}
                  x={x}
                  y={y}
                  width={1}
                  height={1}
                  fill={fill}
                />
              );
            })
          )}
          {/* Six Hats hat overlay (PA-hat): avatar-ийн дээд хэсэгт (y=0..1) өнгөт малгай харуулна */}
          {hatColor && (
            <>
              {/* Малгайн бие (crown) */}
              <rect x={4} y={0} width={8} height={1} fill={hatColor} opacity={0.95} />
              {/* Малгайн дүрс (brim) */}
              <rect x={2} y={1} width={12} height={1} fill={hatColor} opacity={0.85} />
            </>
          )}
        </svg>
      </motion.div>
    </div>
  );
}
